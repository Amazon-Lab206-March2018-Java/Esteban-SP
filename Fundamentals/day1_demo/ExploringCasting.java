public class ExploringCasting {
    public static void main(String[] args) {
        int i = 10;
        double j = 13.3456789;
        double newI = (double) i;
        int newJ = (int) j;
        System.out.println("i: " + i + " j: " + j + " newI: " + newI + " newJ: " + newJ);

        int x = 35;
        float f = x;
        System.out.println("the number is: " + f);

    }
}
